    public static void mapMaker(int[] maps) {

        int locationX;
        int locationY;
        int mapNum;

        for (int i = 0; i < maps.length; i++) {
            locationX = 40 * (i % 26);
            locationY = 40 * (i / 26);

            GameSpriteBricks sb1;

            mapNum = maps[i];
            passMapIndex = i;
            switch (mapNum) {
                case 0:
                    break;
                case 1:
                    sb1 = new GameSpriteBricks(locationX, locationY, GameStarter.block1);
                    mapHelper(sb1, mapNum, 50);
                    break;
                case 2:
                    sb1 = new GameSpriteBricks(locationX, locationY, GameStarter.block2);
                    mapHelper(sb1, mapNum, 100);
                    break;
                case 3:
                    sb1 = new GameSpriteBricks(locationX, locationY, GameStarter.block3);
                    mapHelper(sb1, mapNum, 150);
                    break;
                case 4:
                    sb1 = new GameSpriteBricks(locationX, locationY, GameStarter.block4);
                    mapHelper(sb1, mapNum, 200);
                    break;

                case 5:
                    sb1 = new GameSpriteBricks(locationX, locationY, GameStarter.KrakenBig);
                    sb1.setBrickLifeCount(3);
                    mapHelper(sb1, mapNum, 500);
                    GameStarter.krakenCount++;
                    break;
                case 6:
                    sb1 = new GameSpriteBricks(locationX + 20, locationY, GameStarter.KrakenSmall);
                    sb1.setBrickLifeCount(2);
                    mapHelper(sb1, mapNum, 300);
                    GameStarter.krakenCount++;
                    break;
                case 7:
                    sb1 = new GameSpriteBricks(locationX, locationY, GameStarter.UnbreakableLong);
                    mapHelper(sb1, mapNum, 0);
                    break;
                case 8:
                    sb1 = new GameSpriteBricks(locationX, locationY, GameStarter.unbreakableBlock);
                    mapHelper(sb1, mapNum, 0);
                    break;
                case 9:
                    sb1 = new GameSpriteBricks(locationX, locationY, GameStarter.PowerUp);
                    mapHelper(sb1, mapNum, 0);
                    break;
                case 10:
                    sb1 = new GameSpriteBricks(locationX, locationY, GameStarter.blockSplit);
                    mapHelper(sb1, mapNum, 0);
                    break;
            }

        }
    }


    private static void mapHelper(GameSpriteBricks sb1, int i, int brickPointValue) {
        sb1.setBrickType(i);
        sb1.debugBrickNum = passMapIndex;
        sb1.setBrickPointValue(brickPointValue);

        if (i == 7 || i == 8) {

            sb1.setBreakableTruth(false);

        }

        Collision.blocks.add(sb1);
        GameStarter.geobv.addObserver(sb1);

    }

    public static void deleteMap() {
        GameSpriteBricks tempBrick;

        while (!Collision.blocks.isEmpty()) {
            tempBrick = Collision.blocks.get(0);
            GameStarter.geobv.deleteObserver(tempBrick);
            Collision.blocks.remove(tempBrick);

        }

    }

